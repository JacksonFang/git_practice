def add_three_nums(num1, num2, num3):
    return num1 + num2 + num3

def main():
    num1 = 3
    num2 = 4
    num3 = 5

    result = add_three_nums(num1, num2, num3)
    print(result)  # Output: 12
    print(result)  # Output: 12
    print(result)  # Output: 12


if __name__ == '__main__':
    main()
